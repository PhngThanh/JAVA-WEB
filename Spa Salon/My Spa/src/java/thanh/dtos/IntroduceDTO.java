package thanh.dtos;

import java.io.Serializable;

/**
 *
 * @author THANH
 */
public class IntroduceDTO implements Serializable{
   private int id;
   private String title;
   private String des;
   private String date;
   private String img;

    public IntroduceDTO() {
    }

    public IntroduceDTO(int id, String title, String des, String date, String img) {
        this.id = id;
        this.title = title;
        this.des = des;
        this.date = date;
        this.img = img;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @param title the title to set
     */
    public void setTitle(String title) {
        this.title = title;
    }

    /**
     * @return the des
     */
    public String getDes() {
        return des;
    }

    /**
     * @param des the des to set
     */
    public void setDes(String des) {
        this.des = des;
    }

    /**
     * @return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }

    /**
     * @return the img
     */
    public String getImg() {
        return img;
    }

    /**
     * @param img the img to set
     */
    public void setImg(String img) {
        this.img = img;
    }
   
   
}
